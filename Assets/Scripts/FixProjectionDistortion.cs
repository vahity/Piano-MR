using UnityEngine;

[RequireComponent(typeof(Camera))]
public class FixProjectionDistortion : MonoBehaviour
{
    public float verticalScale = 0.85f;  // ?? ???? ??? ??? ???? ???? ?????? ???? ????? ???

    void Start()
    {
        Camera cam = GetComponent<Camera>();
        Matrix4x4 proj = cam.projectionMatrix;

        // ???? ????? ????? FOV (??????)
        proj[1, 1] *= verticalScale;

        cam.projectionMatrix = proj;
    }
}
